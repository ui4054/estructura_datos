    private void GUARDARActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_GUARDARActionPerformed

        try {
            String nombre = "";

            JFileChooser file = new JFileChooser();
            file.setDialogTitle("Guardar Archivo de Texto");
            file.showSaveDialog(this);
            File guarda = file.getSelectedFile();

            if (guarda != null) {
                /*
                 * guardamos el archivo y le damos el formato directamente, si queremos que se
                 * guarde en formato doc lo definimos como .doc
                 */
                FileWriter save = new FileWriter(guarda);
                save.write(Presentacion.CampoTexto.getText());
                save.close();
                JOptionPane.showMessageDialog(null, "Archivo Guardado", "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex + "" + "\nNo se ha encontrado el archivo", "ADVERTENCIA!!!",
                    JOptionPane.WARNING_MESSAGE);
        }
    }// GEN-LAST:event_GUARDARActionPerformed

